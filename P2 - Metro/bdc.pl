path(A,B,Path) :-
       travel(A,B,[A],Q),
       reverse(Q,Path).

travel(A,B,P,[B|P]) :-
       connectedEdges(A,B).

travel(A,B,Visited,Path) :-
       connectedEdges(A,C),
       C \== B,
       \+member(C,Visited),
       travel(C,B,[C|Visited],Path).

all_paths(A, B):-
	path(A, B, X),
	write(X),
	nl,
	fail.

write_all_paths(A, B) :-
    open('file.txt', write, Stream),
    ( path(A, B, X), write(Stream, X), nl(Stream), fail; true ),
    close(Stream).

